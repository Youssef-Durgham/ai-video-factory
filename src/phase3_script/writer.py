WRITER_SYSTEM = """أنت كاتب سكربتات وثائقية وإخبارية محترف بأسلوب القنوات العربية الكبرى (الجزيرة الوثائقية، العربي، Step News).

السكربت الذي تكتبه سيُقرأ بواسطة نموذج ذكاء اصطناعي صوتي (TTS)، ثم يُحوَّل لفيديو وثائقي.

═══════════════════════════════════════════
الجزء الأول: الهيكلية والأسلوب الوثائقي
═══════════════════════════════════════════

### ١. الخطاف (The Hook)
- ابدأ **دائماً** بمشهد درامي، حقيقة صادمة، أو تناقض غريب في أول ثلاثين ثانية.
- الخطاف يحدد إذا المشاهد سيبقى أو يغادر.
- أمثلة:
  "في صباح الثاني والعشرين من سبتمبر... استيقظت مدينة كاملة لتجد أنها لم تعد موجودة على الخريطة."
  "ماذا لو أخبرتك أن تحت قدميك الآن... يوجد محيط كامل لم يره أحد قط؟"

### ٢. السرد القصصي الدرامي
- حوّل المعلومات الجافة إلى **قصة** لها حبكة وذروة وحل.
- استخدم لغة بليغة مليئة بالاستعارات والتشبيهات العميقة.
- لا تسرد حقائق — احكِ قصة.
- مثال سيء: "البركان ثار عام ألف وتسعمئة وثمانين."
- مثال جيد: "في صباح ربيعي هادئ... قررت الأرض أن تفتح فمها. حممٌ كالأنهار الحمراء ابتلعت كل شيء في طريقها."

### ٣. الإيقاع (Pacing)
- نوّع بين الجمل القصيرة للتوتر والجمل الطويلة للتحليل.
- بعد كل فقرة تحليلية طويلة، ضع جملة قصيرة حادة:
  "عقود من البحث. ملايين الدولارات. آلاف الباحثين... والنتيجة؟ لا شيء."
- الإيقاع مثل الموسيقى: هدوء، تصاعد، ذروة، سكون.

### ٤. الخاتمة الدائرية
- اربط نهاية السكربت ببدايته (الخطاف).
- اختم بتساؤل فلسفي أو عميق يترك المشاهد يفكر.
- مثال: إذا بدأت بـ "مدينة اختفت من الخريطة"، اختم بـ "وربما... نحن أيضاً على خريطة أحدهم، ننتظر أن نُمحى."

═══════════════════════════════════════════
الجزء الثاني: التعليمات الإخراجية (Cues)
═══════════════════════════════════════════

اكتب تعليمات بصرية وصوتية بين أقواس مربعة قبل كل فقرة سردية:

[بصري: لقطة جوية بطيئة لمدينة مهجورة عند الفجر، ضباب خفيف، ألوان باردة]
[صوتي: صمت لثانيتين ثم موسيقى غامضة هادئة، صوت رياح بعيدة]

هذه التعليمات ستُستخدم لتوجيه نظام توليد الفيديو والموسيقى والمؤثرات.

═══════════════════════════════════════════
الجزء الثالث: هندسة اللفظ للذكاء الاصطناعي الصوتي (TTS)
═══════════════════════════════════════════

⚠️ هذا الجزء حاسم — السكربت سيُقرأ بواسطة معلق AI صوتي.

### ١. التشكيل الجزئي الذكي
ضع الحركات **فقط** عند الضرورة:
- ✅ الأفعال المبنية للمجهول: "قُتِلَ"، "يُعْتَقَدُ"، "اكتُشِفَ"
- ✅ الكلمات المشتركة التي يختلف نطقها: "عَقْد" (اتفاق) مقابل "عُقَد" (جمع)
- ✅ الأسماء الأعجمية المعقدة: "تشيرنوبِل"، "كراكاتوا"
- ✅ أواخر الكلمات في الجمل المفصلية لضمان الوقف الصحيح
- ❌ **ممنوع تماماً** التشكيل الكامل لكل حرف — هذا يربك المعلق الصوتي ويجعله يبالغ في النطق

### ٢. الكتل الصوتية
المعلق الصوتي يقرأ "كتلاً" لا كلمات منفصلة:
- المضاف والمضاف إليه = كتلة واحدة: "صراع إرادات"، "نظام بيئي"
- الصفة واسمها = كتلة واحدة: "حرب أهلية مدمرة"
- حرف الجر وما بعده = كتلة واحدة: "في قلب المحيط"
- **لا تفصل** داخل الكتلة بأي علامة ترقيم أو وقفة

### ٣. الوقفات الذكية
- الوقفة تكون **قبل** الكتلة المهمة، لا داخلها ولا بعدها:
  ✅ "اكتشفوا... نظاماً بيئياً فريداً"
  ❌ "اكتشفوا نظاماً... بيئياً فريداً"
- استخدم ثلاث نقاط (...) للوقفات الدرامية قبل المفاجآت:
  "لم يكن هذا بركاناً عادياً... كان أضخم انفجار في التاريخ المسجل."
- استخدم النقطة (.) للوقفات القصيرة بين الجمل.
- استخدم الفاصلة (،) للوقفات الخفيفة داخل الجملة.

### ٤. الأرقام والتواريخ
تُكتب **دائماً** بالحروف العربية:
- ❌ "عام 1980" ← المعلق لن ينطقها صحيحاً
- ✅ "عام ألف وتسعمئة وثمانين"
- ❌ "500 كم"
- ✅ "خمسمئة كيلومتر"
- ❌ "30%"
- ✅ "ثلاثون بالمئة"

═══════════════════════════════════════════
الجزء الرابع: القواعد الذهبية
═══════════════════════════════════════════

1. اللغة: عربية فصحى معاصرة راقية — **لا كلمة واحدة بالإنجليزية**.
2. كل معلومة تُنسب: "في عام..."، "وفقاً لـ..."، "تشير الدراسات إلى..."
3. لا حشو كلمات مفتاحية — المحتوى هو الملك.
4. كل مشهد يضيف معلومة **جديدة** — لا تكرار بصياغات مختلفة.
5. لا جمل أطول من خمس وعشرين كلمة بدون فاصلة أو وقفة.
6. نوّع الإيقاع: جمل قصيرة بعد طويلة. سؤال بعد تقرير. صمت بعد صخب.
7. اختم كل مشهد بجملة قصيرة مؤثرة (ثلاث إلى خمس كلمات).

## تحذير صارم
إذا كتبت بالإنجليزية، أو حشوت كلمات مفتاحية، أو شكّلت النص بالكامل، أو فصلت المضاف عن المضاف إليه — سيتم رفض السكربت."""

WRITER_PROMPT = """# 🎬 اكتب سكربت وثائقي احترافي

## 📌 الموضوع
{topic}

## 🎯 الزاوية
{angle}

## ⏱️ المدة
{target_minutes} دقائق ({target_words} كلمة)

---

## البنية المطلوبة (تسعة مشاهد)

كل مشهد يُكتب بهذا الشكل:

```
المشهد X: [عنوان المشهد]

[بصري: وصف اللقطات، الكاميرا، الإضاءة، الألوان]
[صوتي: وصف الموسيقى، المؤثرات، الإيقاع العام]

🎙️ السرد:
(مئة إلى مئة وخمسين كلمة — نص المعلق الصوتي الكامل)
```

### المشاهد التسعة:

**المشهد الأول: الخطاف (الثانية صفر إلى الثانية خمس وأربعين)**
جذب فوري — حقيقة صادمة أو مشهد درامي يشد الانتباه.

**المشهد الثاني: بناء الغموض (خمس وأربعون ثانية إلى دقيقة ونصف)**
أول اكتشاف، أسئلة مفتوحة، لغة درامية.

**المشهد الثالث: الرحلة (دقيقة ونصف إلى دقيقتين وربع)**
قصص استكشاف حقيقية، صعوبات، لحظات مفصلية.

**المشهد الرابع: العلم مقابل الغموض (دقيقتان وربع إلى ثلاث دقائق)**
فرضيات متضاربة، اكتشافات، "بعض العلماء يعتقدون... وآخرون يقولون..."

**المشهد الخامس: التفسير العلمي (ثلاث دقائق إلى ثلاث وخمس وأربعين ثانية)**
شرح العلوم بلغة يومية وتشبيهات ممتعة.

**المشهد السادس: الجانب الإنساني (ثلاث وخمس وأربعون ثانية إلى أربع ونصف)**
أساطير، حكايات شعبية، البُعد الثقافي.

**المشهد السابع: التأثير (أربع دقائق ونصف إلى خمس وربع)**
ربط بالحياة الواقعية، أرقام وإحصائيات.

**المشهد الثامن: الذروة الدرامية (خمس دقائق وربع إلى ست دقائق)**
الحدث الأكبر، اللحظة الحاسمة، الشهادات.

**المشهد التاسع: الخاتمة الدائرية (ست دقائق إلى ثمان دقائق)**
تأمل، ربط بالخطاف الأول، سؤال فلسفي عميق، دعوة قصيرة.

---

## الحقائق المرجعية

{research_text}

---

## ابدأ الآن!

اكتب السكربت كاملاً — تسعة مشاهد — بالأسلوب الوثائقي العربي الاحترافي.
تذكر: التشكيل الجزئي فقط عند الضرورة، الأرقام بالحروف، الكتل الصوتية لا تُفصَل.

**السكربت الممتاز = فيديو ناجح.** 🎬
"""


class ScriptWriter:
    """Professional Arabic documentary script writer using Qwen 3.5-27B."""

    def __init__(self, config: dict):
        self.config = config

    def write_script(
        self,
        topic: str,
        research: dict,
        seo_data: dict,
        channel_config: dict,
        performance_rules: list[dict] = None,
    ) -> str:
        """Write a professional Arabic documentary script."""
        title = seo_data.get("selected_title", f"الحقيقة عن {topic}")
        channel_name = channel_config.get("name", "وثائقيات")

        target_min = channel_config.get("content", {}).get("target_length_min", [8, 12])
        if isinstance(target_min, list):
            target_minutes = (target_min[0] + target_min[1]) // 2
        else:
            target_minutes = target_min
        target_words = target_minutes * 130

        research_text = research.get("research_text", f"الموضوع: {topic}")
        if len(research_text) > 6000:
            research_text = research_text[:6000] + "\n\n[... المزيد من التفاصيل ...]"

        angle = research.get("angle", "تحليل شامل مع قصص استكشاف")

        prompt = WRITER_PROMPT.format(
            topic=topic,
            angle=angle,
            target_minutes=target_minutes,
            target_words=target_words,
            research_text=research_text,
        )

        result = generate(
            prompt=prompt,
            system=WRITER_SYSTEM,
            config=self.config,
            max_tokens=8192,
            temperature=0.85,
        )

        if not result:
            return ""

        script_text = self._extract_narration(result)
        word_count = len(script_text.split())

        if word_count < target_words * 0.6:
            logger.warning(f"Script too short ({word_count} words vs target {target_words})")
            result2 = generate(
                prompt=f"""السكربت السابق ({word_count} كلمة) أقصر من المطلوب {target_words} كلمة.
وسّع كل مشهد بتفاصيل إضافية وقصص أعمق.
أعد كتابة السكربت كاملاً بالطول المطلوب.""",
                system="أنت كاتب وثائقي. وسّع السكربت بتفاصيل عميقة.",
                config=self.config,
                max_tokens=8192,
                temperature=0.85,
            )
            if result2 and len(result2.split()) > word_count:
                script_text = self._extract_narration(result2)

        return script_text

    def _extract_narration(self, raw_script: str) -> str:
        """Extract narration text from structured script (remove cues/headers)."""
        lines = raw_script.strip().split("\n")
        narration_lines = []
        in_narration = False

        for line in lines:
            stripped = line.strip()
            if "السرد:" in stripped or "🎙️" in stripped:
                in_narration = True
                continue
            elif stripped.startswith("[") and stripped.endswith("]"):
                in_narration = False
                continue
            elif stripped.startswith("المشهد") or stripped.startswith("##") or stripped.startswith("```"):
                in_narration = False
                continue
            elif stripped.startswith("📹") or stripped.startswith("🔊"):
                in_narration = False
                continue

            if in_narration and stripped:
                narration_lines.append(stripped)

        if narration_lines:
            return "\n\n".join(narration_lines)

        # Fallback: return everything (no structure detected)
        return raw_script
